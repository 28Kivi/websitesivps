const LanguageSelector = () => {
  const { i18n } = useTranslation();
  const [currentLang, setCurrentLang] = useState(i18n.language || 'en');

  useEffect(() => {
    // i18n dil deÄŸiÅŸikliklerini dinle
    const handleLanguageChanged = (lng) => {
      const langCode = lng?.split('-')[0] || lng || 'en';
      setCurrentLang(langCode);
    };

    i18n.on('languageChanged', handleLanguageChanged);
    
    // Ä°lk yÃ¼klemede mevcut dili ayarla
    setCurrentLang(i18n.language?.split('-')[0] || i18n.language || 'en');

    return () => {
      i18n.off('languageChanged', handleLanguageChanged);
    };
  }, [i18n]);

  const changeLanguage = useCallback((lng) => {
    logger.log('Changing language to:', lng);
    i18n.changeLanguage(lng).then(() => {
      logger.log('Language changed to:', lng);
      setCurrentLang(lng);
      localStorage.setItem('i18nextLng', lng);
    }).catch((err) => {
      logger.error('Language change error:', err);
    });
  }, [i18n]);

  return (
    <div className="language-selector">
      <select
        value={currentLang}
        onChange={(e) => changeLanguage(e.target.value)}
        className="language-select"
      >
        {languages.map((lang) => (
          <option key={lang.code} value={lang.code}>
            {lang.flag} {lang.name}
          </option>
        ))}
      </select>
    </div>
  );
};

export default LanguageSelector;

